#!/bin/python3

a_list = [1,2,3,4,5,6,7,8,9]
print(a_list) # the whole list []
print(a_list[0]) # first value
print(a_list[1]) # last value
print(a_list[0:3]) # the first three values
print(a_list[0::2]) # all the values jumping two values

a_str = "Hello world!!!"
print(a_str)
print(a_str[0]) # first character
print(a_str[1]) # second character
print(a_str[::-1]) # reversed string

